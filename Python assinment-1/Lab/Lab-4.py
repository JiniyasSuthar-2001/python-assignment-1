# Program to demonstrate variables and different data types

# Integer
age = 20

# Float
height = 5.9

# String
name = "Jiniyas"


# List
fruits = ["apple", "banana", "mango"]

# Tuple
coordinates = (10, 20)

# Dictionary
person = {"name": "Jiniyas", "age": 20, "city": "Himatnagar"}

# Set
unique_numbers = {1, 2, 3, 3, 2, 1}

# Printing all variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Fruits List:", fruits)
print("Coordinates Tuple:", coordinates)
print("Person Dictionary:", person)
print("Unique Numbers Set:", unique_numbers)
